package vulkan

// Offset2D - Structure specifying a two-dimensional offset
type Offset2D struct {
	// X is the x offset.
	X int32

	// Y is the y offset.
	Y int32
}

// Offset3D - Structure specifying a three-dimensional offset
type Offset3D struct {
	// X is the x offset.
	X int32

	// Y is the y offset.
	Y int32

	// Z is the z offset.
	Z int32
}
